public class Perro {

    private String nombre;

    private String raza;

    private int edad;

    private String tamanio;
    
    public Perro() {
    }
      public Perro(String nombre, String raza, int edad, String tamanio) {
    }
    
    public String getNombre() {
        return nombre;
    }

    public String getRaza() {
        return raza;
    }

    public int getEdad() {
        return edad;
    }

    public String getTamanio() {
        return tamanio;
    }
  
    
}